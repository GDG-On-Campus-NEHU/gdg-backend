def env_bool(name, default=False):
    value = os.getenv(name)
    if value is None:
        return default
    return value.strip().lower() in {'1', 'true', 'yes', 'on'}


def env_list(name, default=None):
    value = os.getenv(name)
    if value is None:
        return default if default is not None else []
    return [item.strip() for item in value.split(',') if item.strip()]


def database_config_from_url(database_url):
    parsed = urlparse(database_url)
    scheme = parsed.scheme.lower()

    if scheme in {'postgres', 'postgresql'}:
        engine = 'django.db.backends.postgresql'
    elif scheme in {'mysql', 'mysql2'}:
        engine = 'django.db.backends.mysql'
    elif scheme in {'sqlite', 'sqlite3'}:
        engine = 'django.db.backends.sqlite3'
    else:
        raise ValueError(f'Unsupported DATABASE_URL scheme: {scheme}')

    if engine == 'django.db.backends.sqlite3':
        db_path = unquote(parsed.path) if parsed.path else ''
        if db_path.startswith('/'):
            db_path = db_path[1:]
        if not db_path:
            db_path = 'db.sqlite3'
        return {
            'ENGINE': engine,
            'NAME': str((BASE_DIR / db_path).resolve()) if not os.path.isabs(db_path) else db_path,
        }

    config = {
        'ENGINE': engine,
        'NAME': unquote(parsed.path.lstrip('/')),
        'USER': unquote(parsed.username or ''),
        'PASSWORD': unquote(parsed.password or ''),
        'HOST': parsed.hostname or '',
        'PORT': str(parsed.port or ''),
    }

    options = dict(parse_qsl(parsed.query, keep_blank_values=True))
    if options:
        config['OPTIONS'] = options

    return config

